// الرسم العثماني يكثر فيه الألف الخنجرية (ٰ). نولّد صيغتين: إحداها بحذفها
// (الرحمٰن→الرحمن) والأخرى بقلبها ألفاً (مهٰدًا→مهادا) كي يطابق القاموسُ الرسمَين.
function _strip(s) {
  return String(s)
    .replace(/[\u06D6-\u06ED]/g, '')            // علامات وقف ورموز مصحفية
    .replace(/[\u08F0-\u08F2]/g, '')            // تنوين علوي
    .replace(/\u0640/g, '')                     // تطويل
    .replace(/[\u200B-\u200F]/g, '')            // فواصل خفية
    .replace(/[\u064B-\u065F]/g, '')            // تشكيل (عدا الخنجرية)
    .replace(/[أإآٱ]/g, 'ا')
    .replace(/ؤ/g, 'و').replace(/ئ/g, 'ي')
    .replace(/ة/g, 'ه').replace(/ى/g, 'ي')
    .replace(/ء/g, '')
    .trim();
}
function normalizeAr(w)      { return _strip(String(w || '').replace(/\u0670/g, '')); }   // حذف الخنجرية
function normalizeArAlef(w)  { return _strip(String(w || '').replace(/\u0670/g, 'ا')); }  // قلبها ألفاً

// يولّد قائمة مرشّحات بتجريد السوابق تدريجياً (للبحث في القاموس)
function clitics(norm) {
  const set = new Set([norm]);
  const SUF = ['هم','هن','كم','كن','نا','ها','هو','هي','ه','ك','ي','ون','ين','ات','وا','تم','تن','ا'];
  const PREF_AL = (x) => {
    const c = [];
    if (x.startsWith('لل') && x.length > 3) c.push('ال' + x.slice(2), x.slice(2));
    if (x.startsWith('ال') && x.length > 3) c.push(x.slice(2));
    return c;
  };
  const prefixed = new Set([norm]);
  if (/^[وف]/.test(norm) && norm.length > 2) prefixed.add(norm.slice(1));
  for (const base of [...prefixed]) {
    PREF_AL(base).forEach(x => prefixed.add(x));
    if (/^[بكل]/.test(base) && base.length >= 3) {
      const r = base.slice(1); prefixed.add(r);
      PREF_AL(r).forEach(x => prefixed.add(x));
    }
  }
  prefixed.forEach(x => set.add(x));
  for (let pass = 0; pass < 2; pass++) {
    for (const base of [...set]) {
      for (const suf of SUF) {
        if (base.endsWith(suf) && base.length - suf.length >= 2) set.add(base.slice(0, -suf.length));
      }
    }
  }
  return [...set];
}

