function lookupWordMeaning(text) {
  const cands = new Set();
  for (const norm of [normalizeAr(text), normalizeArAlef(text)]) {
    if (norm) for (const c of clitics(norm)) cands.add(c);
  }
  for (const c of cands) if (DICT[c]) return DICT[c];
  return null;
}

Object.assign(window, { lookupWordMeaning, normalizeAr, AFN_WORD_DICT: DICT });
