function hadithOfDay() {
  const N = HADITHS.length;
  const now = new Date();
  const day = Math.floor(Date.UTC(now.getFullYear(), now.getMonth(), now.getDate()) / 86400000);
  const pick = (d) => (((d * 2654435761) % N) + N) % N;
  let idx = pick(day);
  if (pick(day - 1) === idx) idx = (idx + 1) % N;   // لا يتكرّر يومين متتاليين
  return HADITHS[idx];
}

