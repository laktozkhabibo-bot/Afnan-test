const DICT = {};
for (const [k, v] of Object.entries(RAW)) { const n = normalizeAr(k); if (n) DICT[n] = v; }
(function foldBundled() {
  try {
    if (typeof SURAHS === 'undefined') return;
    for (const num of Object.keys(SURAHS)) {
      for (const ayah of SURAHS[num].ayahs) {
        for (const wd of ayah) {
          if (!wd.m) continue;
          const n = normalizeAr(wd.t);
          if (n && !DICT[n]) DICT[n] = wd.m;
        }
      }
    }
  } catch (e) {}
})();

